function out = runLengthEncode(ascii,char_num)
    % Initialize an empty output array
    out = [];
    
    % Iterate through the input array
    n = length(ascii);
    i = 1;
    doze_num = 0;
    while i <= n
        if ascii(i)~=0x00
            out =[out,ascii(i)];
            if doze_num == char_num
                doze_num = 0;
            end    
            doze_num = doze_num +1;            
        else
            % Count the number of consecutive occurrences of the current character
            count = 1;
            doze_num = doze_num +1;
            while i+1 <= n && ascii(i+1) == ascii(i)  
                if doze_num == char_num
                    doze_num = 0;
                    break
                end                   
                i = i + 1;
                count = count + 1;
                doze_num = doze_num +1;                   
            end    
            % Convert the count to two bytes (high and low)
            % num_high = bitshift(count, -8); % High byte (most significant byte)
            % num_low = mod(count, 256);      % Low byte (least significant byte)
            num = count;
            
            % Append the high byte, low byte, and character to the output array
            % out = [out, num_high, num_low, ascii(i)];
            out =[out,ascii(i),num];            
        end                
        % Move to the next unique character
        i = i + 1;
    end
end
